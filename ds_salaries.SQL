SELECT * FROM ds_salaries;
-- ANALYZE
-- SALARY ANALYSIS 

-- .1 HIGHEST SALARY
-- .2 AVRAGE SALARY 
-- .3 MINIMUM SALARY

SELECT MAX(SALARY)AS HIGHEST_SALARY, AVG(SALARY)AS AVERAGE_SALARY, MIN(SALARY)AS LOWEST_SALARY FROM ds_salaries; 

-- .4 TOP 5 HIGHEST SALARY

SELECT job_title, SALARY FROM  ds_salaries
ORDER BY SALARY DESC 
LIMIT 5;
SELECT JOB_TITLE, COUNT(*) FROM ds_salaries
GROUP BY job_title;
SELECT JOB_TITLE , MAX(SALARY) FROM ds_salaries
GROUP BY job_title
LIMIT 10;

-- 5 HIGHEST EARNING JOB TITLE 

SELECT JOB_TITLE , ROUND(AVG(SALARY_IN_USD),2) AS AVG_SALARY
FROM ds_salaries
GROUP BY JOB_TITLE
ORDER BY AVG_SALARY DESC
LIMIT 10;

-- WHICH EXPERIENCE LEVEL EARN THE MOST 

SELECT EXPERIENCE_LEVEL, COUNT(*) FROM ds_salaries
GROUP BY experience_level;

SELECT EXPERIENCE_LEVEL, 
		ROUND(AVG(SALARY_IN_USD),2) AS AVG_COUNTRY_SALARY
FROM ds_salaries
GROUP BY experience_level
ORDER BY AVG_COUNTRY_SALARY DESC;

-- COUNTRY PAY THE HIGHEST SALARY 

SELECT COMPANY_LOCATION, MAX(SALARY_IN_USD) FROM ds_salaries
GROUP BY COMPANY_LOCATION
LIMIT 10; 

SELECT COMPANY_LOCATION AS COUNTRY , ROUND(AVG(SALARY_IN_USD),2) AS AVG_SALARY FROM ds_salaries
GROUP BY COUNTRY 
ORDER BY AVG_SALARY DESC
LIMIT 5;

-- SALARY INCRESE YEAR BY YEAR 

SELECT WORK_YEAR , MAX(SALARY_IN_USD) AS MAX_SALARY
FROM ds_salaries
GROUP BY work_year; 

-- TOP PAYING COUNTRY FOR DATA SCIENCE
-- MAX
SELECT job_title, company_location , MAX(SALARY) AS HIGHEST_SALARY
FROM ds_salaries
WHERE JOB_TITLE = 'DATA SCIENTIST'
GROUP BY company_location
ORDER BY HIGHEST_SALARY DESC;

-- AVG

SELECT job_title, company_location , ROUND(AVG(SALARY),2) AS HIGHEST_SALARY
FROM ds_salaries
WHERE JOB_TITLE = 'DATA SCIENTIST'
GROUP BY company_location
ORDER BY HIGHEST_SALARY DESC;

